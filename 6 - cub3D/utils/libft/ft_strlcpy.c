#include "libft.h"

size_t	ft_strlcpy(char *dest, const char *src, size_t size)
{
	size_t	x;

	x = 0;
	if (src == NULL)
		return (x);
	x = ft_strlen(src);
	if (x + 1 < size)
		ft_memcpy(dest, src, x + 1);
	else if (size != 0)
	{
		ft_memcpy(dest, src, size - 1);
		dest[size - 1] = '\0';
	}
	return (x);
}
